export function validateVerhoeff(value: string): boolean {
  let c = 0;
  const invertedArray = value.split('').map(Number).reverse();

  for (let i = 0; i < invertedArray.length; i++) {
    c = d[c][p[i % 8][invertedArray[i]]];
  }

  return c === 0;
}
